        <br />
        <b>第十三条</b> 从事营利性体质测定服务的,应当向当地工商行政管理部门办理登记注册并接受其指导、监督和管理。
        <br />
        <b>第十四条</b> 对体质有特殊要求的部门和单位可将《标准》作为招生、招工、保险等体质考核的参考依据。
        <br />
        <b>第十五条</b> 各级体育、教育行政部门及有关部门应当对在《标准》施行工作中做出显著成绩的单位和个人予以表彰奖励。
        <br />
        <b>第十六条</b> 《标准》由国务院体育行政部门负责制定,其中青少年部分由国务院教育行政部门负责制定。
        <br />
        <b>第十七条</b> 有关部门和地方可参照《标准》制定适用于特定人群或地区的体质测定标准。
        <br />
        <b>第十八条</b> 本办法自 2000年7月4 日起施行。
