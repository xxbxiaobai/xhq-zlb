        <br />
        <b>第二条</b> 《标准》适用于 3-69 周岁国民个体的形态、机能和身体素质的测试与评定,按年龄分为幼儿、青少年、成年人和老年人四个部分,其中青少年标准为《学生体质健康标准》。
